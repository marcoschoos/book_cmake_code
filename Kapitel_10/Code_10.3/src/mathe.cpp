#include "mathe.h"

int Mathe::addieren(int a, int b) { return a + b; }

int Mathe::addieren_advanced(int a, int b, int c) {
  return a + b + c;
}

int Mathe::subtrahieren(int a, int b) { return a - b; }

int Mathe::subtrahieren_advanced(int a, int b, int c) {
  return a - b - c;
}

int Mathe::addieren_subtrahieren(int a, int b, int c) {
  return a + b - c;
}

int Mathe::multiplizieren(int a, int b) { return a * b; }
